/**
 * @brief Retrieve the position of touch points
 *
 * @param phi2c pointer of I2C handle
 * @param px pointer of the result x coordinates
 * @param py pointer of the result y coordinates
 * @return number of pointers
 */
int touch_pos(I2C_HandleTypeDef *phi2c, short *px, short *py, short *status) {
	unsigned char addr[5] = { 0x03, 0x09, 0x0f, 0x15, 0x1b }, addr_num = 0x02;
	unsigned char buf[4];
	char suc = 1;
	unsigned char n = 0;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, &addr_num, 1, HAL_MAX_DELAY)
			== 0;
	suc &= HAL_I2C_Master_Receive(phi2c, 0x70, &n, 1, HAL_MAX_DELAY) == 0;
	if (!suc)
		n = 0;
	for (int i = 0; i < n; i++) {
		suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, &addr[i], 1, HAL_MAX_DELAY)
				== 0;
		suc &= HAL_I2C_Master_Receive(phi2c, 0x70, buf, 4, HAL_MAX_DELAY) == 0;
		if (px)
			px[i] = (((buf[2] & 0x0f) << 8) | buf[3]) - ORIGIN_X;
		if (py)
			py[i] = (((buf[0] & 0x0f) << 8) | buf[1]) - ORIGIN_Y;
		if (status)
			status[i] = buf[0] >> 6;
	}
	return suc ? n : -1;
}

