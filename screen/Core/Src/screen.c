unsigned short pixels_565[WINDOW_HEIGHT][WINDOW_WIDTH];
float linespace = 1.5;

/**
 * @brief Print a character
 *
 * @param pos position of the character
 * @param ch the character code
 * @return next position after print
 */
pos_t _putc(pos_t pos, char ch) {
	if (ch == '\n')
		return POS(MARGIN_X, Y(pos) + linespace * MONO_HEIGHT);
	short x = X(pos), y = Y(pos);
	if (x + MONO_WIDTH > WINDOW_WIDTH - MARGIN_X) {
		x = MARGIN_X;
		y += linespace * MONO_HEIGHT;
	}
	if (y + MONO_HEIGHT > WINDOW_HEIGHT - MARGIN_Y)
		y = Y(scrollup(POS(x, y), 1));
	unsigned short (*img)[MONO_WIDTH] =
			(unsigned short (*)[MONO_WIDTH]) monofont[(int) ch];
	for (int i = 0; i < MONO_HEIGHT; i++)
		for (int j = 0; j < MONO_WIDTH; j++)
			pixels_565[y + i][x + j] = img[i][j];
	return POS(x + MONO_WIDTH, y);
}

/**
 * @brief Print a string
 *
 * @param pos position of the string
 * @param s the string
 * @return next position after print
 */
pos_t _puts(pos_t pos, char *s) {
	while (*s)
		pos = _putc(pos, *s++);
	return pos;
}

/**
 * @brief Print an unsigned long integer
 *
 * @param pos position of the integer
 * @param ul the unsigned long integer
 * @param base base of the integer
 *
 * @return next position after print
 */
pos_t _putul(pos_t pos, unsigned long ul, int base) {
	unsigned long div = ul / base;
	char digit = ul % base;
	digit += digit < 10 ? '0' : 'A' - 10;
	return _putc(div ? _putul(pos, div, base) : pos, digit);
}

/**
 * @brief Print a long integer
 *
 * @param pos position of the integer
 * @param l the long integer
 * @param base base of the integer
 *
 * @return next position after print
 */
pos_t _putl(pos_t pos, long l, int base) {
	unsigned long ul;
	if (l >= 0)
		ul = l;
	else {
		ul = -l;
		pos = _putc(pos, '-');
	}
	return _putul(pos, ul, base);
}

/**
 * @brief Print a float number
 *
 * @param pos position of the number
 * @param f the float number
 * @param n length of fraction
 *
 * @return next position after print
 */
pos_t _putf(pos_t pos, float f, int n) {
	if (f < 0) {
		f = -f;
		pos = _putc(pos, '-');
	}
	pos = _putul(pos, (unsigned long) f, 10);
	pos = _putc(pos, '.');
	f = (f - (unsigned long) f) * 10.f;
	for (int i = 0; i < n; i++) {
		pos = _putc(pos, '0' + (unsigned long) f);
		f = (f - (unsigned long) f) * 10.f;
	}
	return pos;
}

/**
 * @brief Clear screen with a specific color
 *
 * @param c the color
 */
pos_t clrscreen(color_t c) {
	for (int i = 0; i < WINDOW_HEIGHT; i++)
		for (int j = 0; j < WINDOW_WIDTH; j++)
			pixels_565[i][j] = c;
	return POS(MARGIN_X, MARGIN_Y);
}

/**
 * @brief move the screen up
 *
 * @param pos current position
 * @param n number of lines to scroll up
 * @return next position after scrolling up
 */
pos_t scrollup(pos_t pos, int n) {
	int d = n * linespace * MONO_HEIGHT;
	for (int i = 0; i < WINDOW_HEIGHT - d; i++)
		for (int j = 0; j < WINDOW_WIDTH; j++)
			pixels_565[i][j] = pixels_565[i + d][j];
	for (int i = WINDOW_HEIGHT - d; i < WINDOW_HEIGHT; i++)
		for (int j = 0; j < WINDOW_WIDTH; j++)
			pixels_565[i][j] = 0xffff;
	return POS(X(pos), Y(pos) - d);
}

/**
 * @brief Fill a rectangle with specific color
 *
 * @param a one point
 * @param b another point
 * @param c the color
 */
void fill_rect(pos_t a, pos_t b, color_t c) {
	int xs = X(a), xe = X(b), ys = Y(a), ye = Y(b);
	for (int i = ys; i < ye; i++)
		for (int j = xs; j < xe; j++)
			pixels_565[i][j] = c;
}

/**
 * @brief Calculate the area proportion in a unit square divided by a line
 *
 * @param x x-coordinate of one point on the line
 * @param y y-coordinate of one point on the line
 * @param cth the cosine of tilt angle
 * @param sth the sine of tile angle
 * @return val one proportion of the separation result
 */
double partial_area(double x, double y, double cth, double sth) {
	x -= (int) x;
	y -= (int) y;
	double l1, l2, l3, l4, k = sth / cth;
	l1 = y - x * k;
	l2 = y + (1 - x) * k;
	l3 = x - y / k;
	l4 = x + (1 - y) / k;
	int b1, b2, b3, b4;
	b1 = -.001 < l1 && l1 < 1.001;
	b2 = -.001 < l2 && l2 < 1.001;
	b3 = -.001 < l3 && l3 < 1.001;
	b4 = -.001 < l4 && l4 < 1.001;
	double ret;
	if (b1 && b2) {
		return cth > .0 ? (l1 + l2) / 2. : 1. - (l1 + l2) / 2.;
	} else {
		if (b1 && b3)
			ret = 1. - l1 * l3 / 2.;
		else if (b1 && b4)
			ret = 1. - (1. - l1) * l4 / 2.;
		else if (b2 && b3)
			ret = l2 * (1. - l3) / 2.;
		else if (b2 && b4)
			ret = (1. - l2) * (1. - l4) / 2.;
		else if (b3 && b4)
			ret = 1. - (l3 + l4) / 2.;
		else
			ret = NAN;
		return sth > .0 ? ret : 1. - ret;
	}
}

/**
 * @brief Draw a line
 *
 * @param a one point
 * @param b another point
 * @param c the color
 * @param stroke the width of the line
 * @param aa whether needs anti-aliasing
 */
void draw_line(point_t a, point_t b, color_t c, double stroke, int aa) {
	double dx = b.x - a.x, dy = b.y - a.y;
	double ox = (a.x + b.x) / 2., oy = (a.y + b.y) / 2.;
	double d = stroke / 2., l = sqrt(dx * dx + dy * dy) / 2.;
	double cth = dx / 2. / l, sth = dy / 2. / l;
	double u, v, step = 1. / sqrt(2.);
	for (u = -d; u < d; u += step)
		for (v = -l; v < l; v += step) {
			int x = ox + u * sth + v * cth, y = oy - u * cth + v * sth;
			if (IN_WINDOW(x, y))
				draw_dot(x, y, c);
		}
	double umax = u, vmax = v;
	double uini[] = { -d, -d, -d - step, umax };
	double ulmt[] = { d, d, -d, umax + step };
	double vini[] = { -l - step, vmax, -l, -l };
	double vlmt[] = { -l, vmax + step, l, l };
	double ustep[] = { step, step, .0, .0 };
	double vstep[] = { .0, .0, step, step };
	double ang1[] = { sth, -sth, -cth, cth };
	double ang2[] = { -cth, cth, -sth, sth };
	for (int i = 0; i < 4; i++) {
		int x_pre = -1, y_pre = -1, c_pre;
		u = uini[i];
		v = vini[i];
		do {
			double x = ox + u * sth + v * cth, y = oy - u * cth + v * sth;
			if (IN_WINDOW(x, y)) {
				int x_tr = (int) x, y_tr = (int) y;
				double s1 = partial_area(x, y, ang1[i], ang2[i]);
				if (x_pre != x_tr || y_pre != y_tr) {
					c_pre = pixels_565[y_tr][x_tr];
					x_pre = x_tr;
					y_pre = y_tr;
				}
				draw_dot(x_tr, y_tr,
						aa ? color_ratio(c_pre, s1) + color_ratio(c, 1. - s1) : c);
			}
			u += ustep[i];
			v += vstep[i];
		} while (u < ulmt[i] && v < vlmt[i]);
	}
}
/**
 * @brief Draw a line
 *
 * @param ct position of center
 * @param r radiuses of the ellipse
 * @param c the color
 * @param stroke the width of the line
 * @param aa whether needs anti-aliasing
 */
void draw_ellipse(point_t ct, point_t r, color_t c, double stroke, int aa) {
	double dpi = 2. * acos(-1), rstep = 1. / sqrt(2.), tstep = rstep
			/ ((r.x > r.y ? r.x : r.y) + (stroke /= 2.)), dr;
	for (dr = -stroke; dr < stroke; dr += rstep)
		for (double t = .0; t < dpi; t += tstep) {
			int x = ct.x + (r.x + dr) * cos(t), y = ct.y + (r.y + dr) * sin(t);
			if (IN_WINDOW(x, y))
				draw_dot(x, y, c);
		}
	double rxbound[] = { r.x - stroke - rstep, r.x + dr };
	double rybound[] = { r.y - stroke - rstep, r.y + dr };
	for (int i = 0; i < 2; i++) {
		int x_pre = -1, y_pre = -1, c_pre;
		for (double t = .0; t < dpi; t += tstep) {
			double cth = cos(t), sth = sin(t);
			double x = ct.x + rxbound[i] * cth, y = ct.y + rybound[i] * sth;
			if (IN_WINDOW(x, y)) {
				int x_tr = (int) x, y_tr = (int) y;
				double s1 = partial_area(x, y, (1 - 2 * i) * sth,
						(2 * i - 1) * cth);
				if (x_pre != x_tr || y_pre != y_tr) {
					c_pre = pixels_565[y_tr][x_tr];
					x_pre = x_tr;
					y_pre = y_tr;
				}
				draw_dot(x_tr, y_tr,
						aa ? color_ratio(c_pre, s1) + color_ratio(c, 1. - s1) : c);
			}
		}
	}
}

int cmppos(const void *a, const void *b) {
	return *(pos_t*) a - *(pos_t*) b;
}

/**
 * @brief Draw a bezier curve
 *
 * @param ord order of bezier curve, 2 (quadratic) or 3 (cubic)
 * @param d minimum distance between two sample points
 * @param n the number of points
 * @param pp the pointer of points, which should contain (order * n + 1) points
 * @param c the color
 * @param pout output array
 */
int calc_bezier(int ord, double d, int n, point_t *pp, color_t c, pos_t *pout) {
	int m = 0;
	double step;
	for (int i = 0; i < n; i++) {
		if (ord == 2) {
			double x0 = pp[2 * i].x, y0 = pp[2 * i].y;
			double x1 = pp[2 * i + 1].x, y1 = pp[2 * i + 1].y;
			double x2 = pp[2 * i + 2].x, y2 = pp[2 * i + 2].y;
			for (double t = 0; t < 1; t += step) {
				double s = 1 - t;
				double dx = fabs(2 * s * (x1 - x0) + 2 * t * (x2 - x1));
				double dy = fabs(2 * s * (y1 - y0) + 2 * t * (y2 - y1));
				step = d / (dx > dy ? dx : dy);
				if (pout) {
					int x = s * s * x0 + 2 * s * t * x1 + t * t * x2;
					int y = s * s * y0 + 2 * s * t * y1 + t * t * y2;
					pout[m] = POS(x, y);
				}
				m++;
			}
		} else if (ord == 3) {
			double x0 = pp[3 * i].x, y0 = pp[3 * i].y;
			double x1 = pp[3 * i + 1].x, y1 = pp[3 * i + 1].y;
			double x2 = pp[3 * i + 2].x, y2 = pp[3 * i + 2].y;
			double x3 = pp[3 * i + 3].x, y3 = pp[3 * i + 3].y;
			for (double t = 0; t < 1; t += step) {
				double s = 1 - t;
				double dx = fabs(
						3 * s * s * (x1 - x0) + 6 * s * t * (x2 - x1)
								+ 3 * t * t * (x3 - x2));
				double dy = fabs(
						3 * s * s * (y1 - y0) + 6 * s * t * (y2 - y1)
								+ 3 * t * t * (y3 - y2));
				step = d / (dx > dy ? dx : dy);
				if (pout) {
					int x = s * s * s * x0 + 3 * s * s * t * x1
							+ 3 * s * t * t * x2 + t * t * t * x3;
					int y = s * s * s * y0 + 3 * s * s * t * y1
							+ 3 * s * t * t * y2 + t * t * t * y3;
					pout[m] = POS(x, y);
				}
				m++;
			}
		}
	}
	return m;
}

typedef struct y_range_struct {
	int yb, ye;
	struct y_range_struct *next;
} y_range_t;

/**
 * @brief Fill a region with border
 *
 * @param n size of border points array
 * @param p border points array
 * @param c color to fill
 */
void fill(int n, pos_t *p, color_t c) {
	qsort(p, n, sizeof(pos_t), cmppos);
	int i = 0, cross_pre = -1, extr = 0;
	y_range_t head = {0, 0, NULL};
	while (i < n) {
		int j = i + 1;
		int x = X(p[i]);
		while (j < n && x == X(p[j]))
			j++; // [i, j) the same x-coordinate
		int cross = 0;
		for (int k = i + 1; k < j; k++)
			if (abs(Y(p[k]) - Y(p[k - 1])) > 1)
				cross++;
		if (cross != cross_pre) {
			cross_pre = cross;
			extr = !extr;
		}
		cross = 0;
		for (int k = i + 1; k < j; k++)
			if (abs(Y(p[k]) - Y(p[k - 1])) > 1) {
				if (cross % 2 == 0) {
					int yb = Y(p[k - 1]), ye = Y(p[k]);
					for (int y = yb + 1; y < ye; y++)
						if (IN_WINDOW(x, y))
							draw_dot(x, y, c);
				}
				if (extr) {
					// should extend more than two column
					// when 'cross' changed, and not changes again yet,
					//   this is the border
					int l = k;
					while (l < j - 1 && abs(Y(p[l + 1]) - Y(p[l])) <= 1)
						l++; // [k, l] is continuous
					int ybl = Y(p[k]), yel = Y(p[l]), ybr = ybl, yer = yel;
					l = i - 1;
					while (abs(x - X(p[l])) == 1 && Y(p[l]) > yel + 1)
						l--;
					while (abs(x - X(p[l])) == 1)
						if (Y(p[l++]) == yel + 1)
							yel++;
					l = i - 1;
					while (abs(x - X(p[l])) == 1)
						if (Y(p[l--]) == ybl - 1)
							ybl--;
					while (abs(x - X(p[l])) == 2
							&& (Y(p[l]) > yel + 1 || Y(p[l]) < ybl - 1))
						l--;
					int left = abs(x - X(p[l])) == 2;
					l = j;
					while (abs(x - X(p[l])) == 1 && Y(p[l]) < ybl - 1)
						l++;
					while (abs(x - X(p[l])) == 1)
						if (Y(p[l--]) == ybr - 1)
							ybr--;
					l = j;
					while (abs(x - X(p[l])) == 1)
						if (Y(p[l++]) == yer + 1)
							yer++;
					while (abs(x - X(p[l])) == 2
							&& (Y(p[l]) > yer + 1 || Y(p[l]) < ybr - 1))
						l++;
					int right = abs(x - X(p[l])) == 2;
					if (!left || !right)
						cross--;
				}
				cross++;
			}
		i = j;
	}
}

/**
 * @brief Initialize the register of control chip of touch panal
 *
 * @param phi2c the pointer of I2C handle
 * @return whether successful
 */
char touch_reg_init(I2C_HandleTypeDef *phi2c) {
	unsigned char buf[3];
	char suc = 1;
	buf[0] = 0x00, buf[1] = 0x00;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, buf, 2, HAL_MAX_DELAY) == 0;
	buf[0] = 0xa4, buf[1] = 0x00;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, buf, 2, HAL_MAX_DELAY) == 0;
	buf[0] = 0x80, buf[1] = 0x46;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, buf, 2, HAL_MAX_DELAY) == 0;
	buf[0] = 0x88, buf[1] = 0x0c;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, buf, 2, HAL_MAX_DELAY) == 0;
	buf[0] = 0xa1, buf[1] = 0x30, buf[2] = 0x03;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, buf, 3, HAL_MAX_DELAY) == 0;
	return suc;
}

/**
 * @brief Retrieve the position of touch points
 *
 * @param phi2c pointer of I2C handle
 * @param px pointer of the result x coordinates
 * @param py pointer of the result y coordinates
 * @return number of pointers
 */
int touch_pos(I2C_HandleTypeDef *phi2c, short *px, short *py, short *status) {
	unsigned char addr[5] = { 0x03, 0x09, 0x0f, 0x15, 0x1b }, addr_num = 0x02;
	unsigned char buf[4];
	char suc = 1;
	unsigned char n = 0;
	suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, &addr_num, 1, HAL_MAX_DELAY)
			== 0;
	suc &= HAL_I2C_Master_Receive(phi2c, 0x70, &n, 1, HAL_MAX_DELAY) == 0;
	if (!suc)
		n = 0;
	for (int i = 0; i < n; i++) {
		suc &= HAL_I2C_Master_Transmit(phi2c, 0x70, &addr[i], 1, HAL_MAX_DELAY)
				== 0;
		suc &= HAL_I2C_Master_Receive(phi2c, 0x70, buf, 4, HAL_MAX_DELAY) == 0;
		if (px)
			px[i] = (((buf[2] & 0x0f) << 8) | buf[3]) - ORIGIN_X;
		if (py)
			py[i] = (((buf[0] & 0x0f) << 8) | buf[1]) - ORIGIN_Y;
		if (status)
			status[i] = buf[0] >> 6;
	}
	return suc ? n : -1;
}

